<h1><p>Dashboard</p></h1>
<p>
Mobile dashboard design for business intelligence has reinvented the concept of monitoring key performance indicators in corporate environments.
</p>
<p>
According to a recent Gartner study, only one third of all business intelligence users used mobile devices to check on their KPIs.
</p>
<p>
As it can be concluded, mobile app dashboard design is the hottest BI trend to look out for, and it is nowhere close to losing that reputation.</p>
<p>
But why is mobile app dashboard design still the gift of very few talented designers? As you can already guess, it is because of complex analytics being processed and displayed on mobile devices.</p>
<p>
The leaders of complex stats will still remain on-premise desktop solutions and their robust comparison dashboards, unless, of course, you dont see this as a competition. The task of mobile dashboards is not to overcome what their desktop counterparts do, but to cover the most important information, and make it accessible anywhere, and on any device.</p>

